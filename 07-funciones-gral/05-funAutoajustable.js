// Función Autoejecutable (IIFE-Immediately Invoked Function Expression)

//Estas funciones se ejecutan tan pronto como se definen.
// Son útiles cuando quieres crear un "alcance" privado 
// para evitar que las variables se mezclen con el resto del código.

(function() {
    console.log("¡Soy una función autoejecutable!");
})();

