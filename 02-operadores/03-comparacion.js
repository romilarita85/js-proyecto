//Operadores de comparacion: se usan para poder comparar
//distintas variables entre si o una variable con otra cosa
//-OPERACIONALES:
let a = 10;
console.log(a > 5);
//Cada vez que utilicemos valores de comparacion van a 
//devolver un boolean (true o false).
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//-DE IGUALDAD:
console.log(a == 10);//preguntamos si los valores son iguales
console.log(a != 10);//distintos

console.log(a === '10');//si queremos saber el valor y el tipo de las variables usamos
console.log(a !== '10');//si queremos saber distinto tipo de dato y de valor



